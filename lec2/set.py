# Створити порожню множину
s = set()

# Додати якісь елементи
s.add(1)
s.add(2)
s.add(3)
s.add(4)
s.add(3)
s.add(1)

# Видалити 2 з множини
s.remove(2)

# Вивести множину
print(s)

# Знайти розмір множини
print(f"У множині {len(s)} елементи")

